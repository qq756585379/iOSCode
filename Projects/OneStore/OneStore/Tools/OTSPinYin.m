+ (NSString *)chineseConvertEnglish:(NSString *)chinese{
    if(!chinese){return nil;}
    NSMutableString *pinYinStr = [NSMutableString stringWithString:chinese];
    //汉字转拼音  先转成带声调的 再转成不带声调的
    //转成带音标(声调)的拼音
    BOOL ret = CFStringTransform((__bridge CFMutableStringRef)pinYinStr, NULL, kCFStringTransformMandarinLatin, NO);
    if(ret)//必须得判断
    {
        NSLog(@"mutableStr = %@",pinYinStr);
        //去掉声调
        BOOL ret1 = CFStringTransform((__bridge CFMutableStringRef) pinYinStr,NULL,kCFStringTransformStripDiacritics, NO);
        if(ret1)//必须得判断
        {
            NSLog(@"mutableStr = %@",pinYinStr);
        }
    }
    //去掉拼音中间的空格
    if([pinYinStr componentsSeparatedByString:@" "].count >2){
        NSArray *tmpArrat = [pinYinStr componentsSeparatedByString:@" "];
        return [tmpArrat componentsJoinedByString:@""];
    }
    return pinYinStr;
}
