+ (NSString *)getFirstLetter:(NSString *)fullString {
    if ([[fullString safeSubstringToIndex:1] canBeConvertedToEncoding:NSASCIIStringEncoding]) {//如果是英语
        return [[fullString safeSubstringToIndex:1] uppercaseString];
